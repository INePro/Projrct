#СОЗДАЕМ ВЕЧНЫЙ ЦИКЛ
while True:
	#ОБВАРАЧИВАЕМ КОД В УСЛОВИЕ, ДЛЯ ИЗБЕЖАНИЯ ОШИБОК
	try:
		chisl = int(input("Введите трехзначное число: "))
		a = chisl // 100     #Отделяем число сотен
		b = chisl // 10 % 10 #Отделяем число десятков
		c = chisl % 10       #Отделяем число единиц
		print(a + b + c)
	#ВЫВОД ТЕКСТА ПОДСКАЗКИ
	except ValueError:
		print("Нужно ввести целое число")
